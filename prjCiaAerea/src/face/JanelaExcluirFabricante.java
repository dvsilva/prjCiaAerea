package face;

import javax.swing.JOptionPane;

import controle.ICtrlManterFabricantes;

/**
 * Implementação da janela de confirmação de exclusão do Fabricante
 * @author Felipe, Amanda e Danyllo
 *
 */
public class JanelaExcluirFabricante {
	/**
	 * Referência para o controlador do caso de uso
	 */
	private ICtrlManterFabricantes ctrl;
	
	/**
	 * Opção escolhida pelo usuário
	 */
	private int opcao;
	
	
	/**
	 * Construtor que irá colocar uma janela modal perguntando
	 * se o usuário deseja ou não excluir o Fabricante
	 * @param nome
	 */
	public JanelaExcluirFabricante(ICtrlManterFabricantes ct, Object selecionado){
		// Guardo a referência para o controlador de caso de uso
		this.ctrl = ct;
		// Pergunto ao usuário o que ele deseja fazer
		this.opcao = JOptionPane.showConfirmDialog(null, "Deseja remover o Fabricante " + selecionado);
		// Verifica o que o usuário indicou para ser feito
		if(this.opcao == JOptionPane.YES_OPTION)
			this.ctrl.excluir();
		else
			this.ctrl.cancelarExcluir();
	}
	
	/**
	 * Retorna a opção indicada pelo usuário
	 * @return
	 */
	public int getOpcao(){
		return this.opcao;
	}
}
